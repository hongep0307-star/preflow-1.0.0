/* ━━━ 원본 이미지 비율 보존 imageSize 계산 ━━━
 * 실제 W/H 비율로 GPT/NB2가 지원하는 3가지 크기 중 가장 가까운 것을 선택.
 * 비표준 크기(1536x1536 등)를 반환하지 않아 GPT API 오류 방지.
 */
function computeImageSizeFromDimensions(w: number, h: number): string {
  const ratio = w / h;
  if (ratio >= 1.4) return "1536x1024"; // 16:9 가로
  if (ratio <= 0.75) return "1024x1536"; // 9:16 세로
  return "1024x1024"; // 정사각형 계열
}

interface CropState {
  _v?: number;
  x: number;
  y: number;
  scale: number;
  rotate?: number;
  fmt?: string;
  ia?: number;
}

type CropMap = Partial<Record<VideoFormat, CropState>>;

function isCropMap(val: unknown): val is CropMap {
  if (!val || typeof val !== "object") return false;
  return "horizontal" in val || "vertical" in val || "square" in val;
}

function getCropForFmt(stored: unknown, fmt: VideoFormat): CropState | null {
  if (!stored) return null;
  if (isCropMap(stored)) return stored[fmt] ?? null;
  const crop = stored as CropState;
  if (crop._v === 2 && (!crop.fmt || crop.fmt === fmt)) return crop;
  return null;
}

function computeImageLayout(
  imgAspect: number,
  containerAspect: number,
  scale: number,
  x: number,
  y: number,
): { wPct: number; hPct: number; leftPct: number; topPct: number } {
  let covWR: number;
  let covHR: number;
  if (imgAspect >= containerAspect) {
    covHR = 1;
    covWR = imgAspect / containerAspect;
  } else {
    covWR = 1;
    covHR = containerAspect / imgAspect;
  }
  const s = scale + 0.2;
  const wPct = s * covWR * 100;
  const hPct = s * covHR * 100;
  return {
    wPct,
    hPct,
    leftPct: 50 - wPct / 2 + x,
    topPct: 50 - hPct / 2 + y,
  };
}

